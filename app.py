        # –ü–ª–∞–Ω–∏—Ä–æ–≤—â–∏–∫ –∑–∞–¥–∞—á
        job_queue = application.job_queue
        
        # –ï–∂–µ–¥–Ω–µ–≤–Ω—ã–µ –ø—É–±–ª–∏–∫–∞—Ü–∏–∏ –ø–æ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—é
        times = ['08:30', '10:00', '12:00', '14:00', '16:00', '18:00', '20:00']

async def handle_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–æ—Ç–∫–∞ –Ω–∞–∂–∞—Ç–∏–π –∫–Ω–æ–ø–æ–∫"""
    query = update.callback_query
    await query.answer()
    
    callback_data = query.data
    
    if callback_data.startswith('test_'):
        # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Ç–µ—Å—Ç–æ–≤
        _, week, answer = callback_data.split('_')
        week = int(week)
        answer = int(answer)
        
        test_data = safety_manager.content_db['express_tests'].get(week)
        if test_data:
            if answer == test_data['correct_answer']:
                message = f"{test_data['explanation']}\n\n‚úÖ –í—ã –æ—Ç–≤–µ—Ç–∏–ª–∏ –ø—Ä–∞–≤–∏–ª—å–Ω–æ!"
            else:
                correct_option = test_data['options'][test_data['correct_answer']]
                message = f"‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ. –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç: {correct_option}\n\n{test_data['explanation']}"
            
            await query.edit_message_text(
                text=message,
                parse_mode='HTML'
            )
    
    elif callback_data.startswith('poll_'):
        # –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ–ø—Ä–æ—Å–æ–≤
        _, week, answer = callback_data.split('_')
        week = int(week)
        answer = int(answer)
        
        poll_data = safety_manager.content_db['weekly_polls'].get(week)
        if poll_data:
            selected_option = poll_data['options'][answer]
            if answer == poll_data['correct_answer']:
                message = f"‚úÖ –í—ã –≤—ã–±—Ä–∞–ª–∏ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –≤–∞—Ä–∏–∞–Ω—Ç: {selected_option}"
            else:
                correct_option = poll_data['options'][poll_data['correct_answer']]
                message = f"‚ùå –í—ã –≤—ã–±—Ä–∞–ª–∏: {selected_option}\n\n–ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç: {correct_option}"
            
            await query.edit_message_text(
                text=f"{poll_data['question']}\n\n{message}",
                parse_mode='HTML'
            )
    
    elif callback_data == 'comment':
        # –ö–Ω–æ–ø–∫–∞ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è
        await query.edit_message_text(
            text=query.message.text + "\n\nüí¨ <b>–î–ª—è –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤ –Ω–∞–ø–∏—à–∏—Ç–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ —á–∞—Ç</b>",
            parse_mode='HTML'
        )

